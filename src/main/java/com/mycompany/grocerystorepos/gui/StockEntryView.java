    private void initTable() {
        String[] columnNames = {"ID", "ProductID", "SupplierID", "Quantity", "UpdateDay"};
        tableModel = new DefaultTableModel(columnNames, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Không cho chỉnh sửa trực tiếp trên bảng
            }
        };
        tbinventory.setModel(tableModel);
    }

    public void loadStockEntries(List<StockEntry> entries) {
        tableModel.setRowCount(0); // 🔥 Xóa dữ liệu cũ trong bảng

        for (StockEntry entry : entries) {
            tableModel.addRow(new Object[]{
                entry.getStockEntryId(),
                entry.getProductId(),
                entry.getSupplierId(),
                entry.getQuantity(),
                entry.getEntryDate().toString()
            });
        }

        tableModel.fireTableDataChanged(); // 🔥 Cập nhật lại bảng
    }

    private void selectRowFromTable() {
        int selectedRow = tbinventory.getSelectedRow();
        if (selectedRow >= 0) {
            txtmasp.setText(tableModel.getValueAt(selectedRow, 1).toString()); // ✅ Chuyển thành String
            txtmancc.setText(tableModel.getValueAt(selectedRow, 2).toString());
            txtsoluong.setText(tableModel.getValueAt(selectedRow, 3).toString());
        }
    }

